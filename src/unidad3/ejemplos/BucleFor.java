package unidad3.ejemplos;

public class BucleFor {

	public static void main(String[] args) {

		int i;// Se puede declarar dentro del for

		for (i = 0; i < 10; i++) {
			System.out.println("Valor de i = " + i + " Buenos días!");
			for (int j = 2; j < 6; j++) {
				System.out.println("Valor de la jota = " + j + " Estamos en programación");
			
			}
		}

	}

}
